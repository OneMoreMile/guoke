public class ClassSpend {

    private String classDate;
    private String classSpent;
    private String desc;

    public String getClassDate() {
        return classDate;
    }

    public void setClassDate(String classDate) {
        this.classDate = classDate;
    }

    public String getClassSpent() {
        return classSpent;
    }

    public void setClassSpent(String classSpent) {
        this.classSpent = classSpent;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
